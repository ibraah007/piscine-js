function trunc(n) {
    if (n >= 0) {
        return Math.floor(n);
    } else {
        return Math.ceil(n);
    }
}

function floor(n) {
    if (n >= 0) {
        return Math.floor(n);
    } else {
        return Math.floor(n);
    }
}

function ceil(n) {
    if (n >= 0) {
        return Math.ceil(n);
    } else {
        return Math.ceil(n);
    }
}

function round(n) {
    return Math.round(n);
}
